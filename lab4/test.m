%% 1. Параметры
Fs = 1000;              % Частота дискретизации, Гц
dt = 1/Fs;
N  = 500;               % Число отсчётов
t  = (0:N-1)' * dt;     % Время (столбец)

%% 2. Создаём сигнал + шум
f_signal = 10;          % Полезный сигнал 10 Гц
x_clean = sin(2*pi*f_signal*t);
xi = -1 + 2*rand(N,1);  % Шум U[-1,1]
x_noisy = x_clean + 0.3*xi;

%% 3. Фурье-образ зашумлённого сигнала
X = fft(x_noisy);

%% 4. Частотная сетка для FFT (в рад/с)
% FFT выдаёт частоты от 0 до Fs, потом "отрицательные"
f_Hz = (0:N-1)' * Fs/N;           % Частота в Гц
w = 2*pi * f_Hz;                   % Частота в рад/с

%% 5. Передаточная функция фильтра (например, ФНЧ 1-го порядка)
fc = 50;  % Частота среза, Гц
sys = tf(1, [1/(2*pi*fc) 1]);  % H(s) = 1/(τs + 1), τ = 1/(2πfc)

%% 6. Вычисляем H(jω) на частотах из FFT
% freqresp ожидает частоты в рад/с и возвращает комплексный отклик
H = freqresp(sys, w);
H = squeeze(H);  % Убираем лишнюю размерность → вектор 500×1

%% 7. Частотная фильтрация: умножение спектров
Y = X .* H;  % Поэлементное умножение

%% 8. Обратное БПФ → отфильтрованный сигнал во времени
y_filtered = ifft(Y);
y_filtered = real(y_filtered);  % Убираем численный шум (мнимая часть ~1e-15)

%% 9. Визуализация
figure('Position', [100 100 1000 600]);

subplot(2,2,1);
plot(t, x_noisy, 'b', 'LineWidth', 0.8); hold on;
plot(t, y_filtered, 'r', 'LineWidth', 1.2);
legend('Зашумлённый вход', 'Выход (частотная фильтрация)');
xlabel('Время, с'); grid on; title('Временной сигнал');

subplot(2,2,2);
f_plot = f_Hz(1:floor(N/2)+1);  % Только положительная частота
X_mag = abs(X(1:floor(N/2)+1));
Y_mag = abs(Y(1:floor(N/2)+1));
plot(f_plot, 20*log10(X_mag+eps), 'b--', ...
     f_plot, 20*log10(Y_mag+eps), 'r-', 'LineWidth', 1);
xlabel('Частота, Гц'); ylabel('Мощность, дБ');
legend('Спектр входа', 'Спектр выхода');
grid on; title('Сравнение спектров');

subplot(2,2,3);
bode(sys); grid on; title('АЧХ фильтра (теория)');

subplot(2,2,4);
plot(t, x_clean, 'g', 'LineWidth', 1); hold on;
plot(t, y_filtered, 'r--', 'LineWidth', 1);
legend('Идеальный сигнал', 'Отфильтрованный');
xlabel('Время, с'); grid on; title('Качество восстановления');